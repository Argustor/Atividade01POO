public class Variaveis {
    public static void TiposVariaveis(){
        System.out.println("Exemplos de variáveis em Java:");
        // int
        int idade = 10;
        System.out.printf("%d é um tipo int.\n", idade);
        // double
        double preco = 10.99;
        System.out.printf("%f é um tipo float\n",preco);
        // char
        char letra = 'a';
        System.out.printf("%c é um tipo char\n", letra);
        // boolean
        boolean verdadeiro = true;
        System.out.println("É um tipo booleano:" + verdadeiro);
        // String
        String nome = "João";
        System.out.printf("%s é um tipo String\n",nome);
    }
}